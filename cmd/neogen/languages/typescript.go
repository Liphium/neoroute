var tsTypeMap = map[neoschema.SchemaType]string{
	neoschema.TypeBool:         "boolean",
	neoschema.TypeByte:         "number",
	neoschema.TypeInt32:        "number",
	neoschema.TypeInt64:        "bigint",
	neoschema.TypeFloat32:      "number",
	neoschema.TypeFloat64:      "number",
	neoschema.TypeString:       "string",
	neoschema.TypeSerializable: "any",
}

func GetTSType(packed neoschema.PackedType) (string, error) {
	switch schema := packed.(type) {
	case *neoschema.StructType:
		return util.ToCamelCase(schema.Name, true), nil
	case *neoschema.ReferenceType:
		return util.ToCamelCase(schema.Object, true), nil
	case *neoschema.ArrayType:
		elem, err := GetTSType(schema.Element)
		if err != nil {
			return "", err
		}
		return elem + "[]", nil
	case *neoschema.MapType:
		k, err := GetTSType(schema.Key)
		if err != nil {
			return "", err
		}
		v, err := GetTSType(schema.Value)
		if err != nil {
			return "", err
		}
		return "Record<" + k + ", " + v + ">", nil
	case *neoschema.NullableType:
		elem, err := GetTSType(schema.Element)
		if err != nil {
			return "", err
		}
		return elem + " | null", nil
	default:
		t, ok := tsTypeMap[schema.Type()]
		if !ok {
			return "", fmt.Errorf("unknown type: %s", schema.Type())
		}
		return t, nil
	}
}

